/**
 * 根据 ID 获取议会成员配置
 *
 * @param id 成员 ID
 * @returns 成员配置，若不存在则返回 undefined
 */
export function getCouncilMemberById(
  id: string
): CouncilMemberConfig | undefined {
  return COUNCIL_MEMBERS.find((m) => m.id === id);
}

/**
 * 根据分类获取议会成员列表
 *
 * @param category 成员分类
 * @returns 该分类下的所有成员
 */
export function getMembersByCategory(
  category: CouncilMemberCategory
): CouncilMemberConfig[] {
  return COUNCIL_MEMBERS.filter((m) => m.category === category);
}

/**
 * 获取所有议会成员分类及其成员
 *
 * @returns 按分类分组的成员映射
 */
export function getMembersGroupedByCategory(): Record<
  CouncilMemberCategory,
  CouncilMemberConfig[]
> {
  const grouped = {} as Record<CouncilMemberCategory, CouncilMemberConfig[]>;
  for (const category of Object.keys(MEMBER_CATEGORY_LABELS) as CouncilMemberCategory[]) {
    grouped[category] = COUNCIL_MEMBERS.filter((m) => m.category === category);
  }
  return grouped;
}

/**
 * 根据组合 ID 获取推荐组合
 *
 * @param id 组合 ID
 * @returns 推荐组合，若不存在则返回 undefined
 */
export function getCombinationById(
  id: string
): RecommendedCombination | undefined {
  return RECOMMENDED_COMBINATIONS.find((c) => c.id === id);
}

/**
 * 根据场景关键词搜索匹配的推荐组合
 *
 * @param scenario 场景关键词
 * @returns 匹配的组合列表
 */
export function findCombinationsByScenario(
  scenario: string
): RecommendedCombination[] {
  const lower = scenario.toLowerCase();
  return RECOMMENDED_COMBINATIONS.filter((c) => {
    const inScenarios = c.applicableScenarios.some((s) =>
      s.toLowerCase().includes(lower)
    );
    const inTags = c.tags.some((t) => t.toLowerCase().includes(lower));
    const inDesc = c.description.toLowerCase().includes(lower);
    return inScenarios || inTags || inDesc;
  });
}

/**
 * 获取推荐组合中所有成员的配置
 *
 * @param combination 推荐组合
 * @returns 成员配置列表（包含议会成员和现有 Agent）
 */
export function getCombinationMembers(
  combination: RecommendedCombination
): CouncilMemberConfig[] {
  return combination.memberIds
    .map((id) => getCouncilMemberById(id))
    .filter((m): m is CouncilMemberConfig => Boolean(m));
}
