/**
 * 根据用户配置生成自定义 Agent 的系统提示词
 *
 * 该函数将用户填写的性格描述、对话风格、专业领域等配置
 * 转化为一段完整的系统提示词，使 AI 能够按照用户的期望
 * 扮演该自定义角色。
 *
 * @param config 用户填写的自定义 Agent 配置
 * @returns 完整的系统提示词字符串
 *
 * @example
 * ```ts
 * const prompt = generateCustomAgentSystemPrompt({
 *   name: '职场教练老王',
 *   personality: '经验丰富的职场老手，直来直去，但心是热的',
 *   dialogueStyle: 'casual',
 *   expertise: ['职业规划', '面试技巧', '职场沟通'],
 *   philosophy: '行动比完美更重要',
 * });
 * ```
 */
export function generateCustomAgentSystemPrompt(
  config: CustomAgentConfig
): string {
  const {
    name,
    nameEn,
    personality,
    dialogueStyle,
    expertise,
    philosophy,
    speakingStyleNote,
    prohibitions,
    targetScenario,
  } = config;

  const styleGuide = STYLE_PROMPT_MAP[dialogueStyle];
  const expertiseList = expertise.map((e) => `- ${e}`).join('\n');
  const prohibitionList =
    prohibitions && prohibitions.length > 0
      ? prohibitions.map((p) => `- ${p}`).join('\n')
      : '';

  return `你是"${name}"${nameEn ? `（${nameEn}）` : ''}，LifeVerse 智慧议会中的一位自定义成员。你不是预设的历史人物或智者，而是用户为自己量身打造的专属顾问。

## 你的性格

${personality}

## 你的核心理念

${philosophy || '以用户的需求为核心，用最适合的方式提供帮助。'}

## 你的专业领域

${expertiseList}

## 你的说话方式

${styleGuide}
${speakingStyleNote ? `\n补充说明：${speakingStyleNote}` : ''}
${targetScenario ? `\n你的目标场景：${targetScenario}` : ''}

## 你在议会中的职责

1. 从你的专业领域出发，为用户提供独到的视角
2. 用你的性格特质为议会讨论增添独特的色彩
3. 与其他成员形成互补或碰撞，丰富讨论的维度
4. 始终以帮助用户为目标，而非展示自己

## 发言要求

- 控制在 150-250 字，密度高于长度
- 保持人格一致性，不要跳出角色
- 用你的专业视角切入，提供其他成员可能忽略的维度
- 如果与其他成员观点不同，坦诚表达但保持尊重
${prohibitionList ? `\n## 禁止行为\n${prohibitionList}` : ''}

## 你相信

你是由用户创造的，你的存在就是为了以用户最需要的方式帮助他/她。你不是完美的，但你是专属的。用你的独特视角，为用户的决策贡献一份力量。`;
}

