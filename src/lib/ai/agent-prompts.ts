/**
 * 获取指定 Agent 的 System Prompt
 *
 * @param agentId Agent ID（如 'musk', 'buffett'）
 * @returns System Prompt 字符串
 * @throws 当 agentId 不在已知列表中时抛出
 */
export function getAgentPrompt(agentId: string): string {
  if (!isAgentId(agentId)) {
    throw new Error(
      `Unknown agentId: "${agentId}". Valid IDs: ${AGENT_IDS.join(', ')}`
    );
  }
  return AGENT_PROMPTS[agentId];
}

/**
 * 类型守卫：判断字符串是否为合法的 AgentId
 */
export function isAgentId(value: string): value is AgentId {
  return (AGENT_IDS as readonly string[]).includes(value);
}

/**
 * 获取所有 Agent 的 Prompt 映射（只读）
 *
 * 用于批量加载或调试。
 */
export function getAllAgentPrompts(): Readonly<Record<AgentId, string>> {
  return AGENT_PROMPTS;
}
